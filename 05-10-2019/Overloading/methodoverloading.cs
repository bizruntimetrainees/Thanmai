public class Methodoveloading    
  {    
    public int add(int a, int b)    
    {    
        return a + b;    
        
    }    
    public int add(int a, int b,int c)  
    {    
        return a + b+c;    
    
    }    
    public float add(float a, float b,float c,float d) 
    {    
        return a + b+c+d;    
    
    }    
  }